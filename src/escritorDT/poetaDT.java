public class poetaDT {
    private int idPoeta;
    private String nombre;
    private String aPaterno;
    private String aMaterno;
    private String email;
    private String pseudonimo;
    private String sexo;
    private String nacionalidad;

    /**
     * @return the idPoeta
     */
    public int getIdPoeta() {
        return idPoeta;
    }

    /**
     * @param idPoeta the idPoeta to set
     */
    public void setIdPoeta(int idPoeta) {
        this.idPoeta = idPoeta;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the aPaterno
     */
    public String getaPaterno() {
        return aPaterno;
    }

    /**
     * @param aPaterno the aPaterno to set
     */
    public void setaPaterno(String aPaterno) {
        this.aPaterno = aPaterno;
    }

    /**
     * @return the aMaterno
     */
    public String getaMaterno() {
        return aMaterno;
    }

    /**
     * @param aMaterno the aMaterno to set
     */
    public void setaMaterno(String aMaterno) {
        this.aMaterno = aMaterno;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the pseudonimo
     */
    public String getPseudonimo() {
        return pseudonimo;
    }

    /**
     * @param pseudonimo the pseudonimo to set
     */
    public void setPseudonimo(String pseudonimo) {
        this.pseudonimo = pseudonimo;
    }

    /**
     * @return the sexo
     */
    public String getSexo() {
        return sexo;
    }

    /**
     * @param sexo the sexo to set
     */
    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    /**
     * @return the nacionalidad
     */
    public String getNacionalidad() {
        return nacionalidad;
    }

    /**
     * @param nacionalidad the nacionalidad to set
     */
    public void setNacionalidad(String nacionalidad) {
        this.nacionalidad = nacionalidad;
    }
}
